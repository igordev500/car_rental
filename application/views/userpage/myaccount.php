            '/index.php/Myaccount/up_bank',
            {
                iban:id_iban,
                bic:id_bic,
                name_holder:id_name_holder,
                address_bank:id_address_bank
            },
            function(data){
                toastr_call('success','Successfully updated!');
            }
        );
    }


    

